alphabet = []
print(len(alphabet))

#appending
alphabet.append("a")
alphabet.append("b")
alphabet.append("c")
print(len(alphabet))

#indexing
alphabet = ["a", "b", "c", "d", "...", "z"]
print(f"The last letter of the alphabet is '{alphabet[-1]}.'")
 
#extending
alphabet = ["a", "b", "c", "d"]
alphabet.extend(["e", "f", "g"])
print(alphabet)

